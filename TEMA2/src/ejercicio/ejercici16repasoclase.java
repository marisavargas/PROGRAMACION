package ejercicio;

import java.util.Scanner;

public class ejercici16repasoclase {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.println(" Introduce una palabra");
		String palabra = "";
		palabra = sc.nextLine();
		int longuitud = palabra.length();

		for (int contador = 0; contador < longuitud; contador++) {

			System.out.println(palabra.substring(contador, contador + 1));
		}
sc.close();
	}

}
